    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $subscriber_name = $request->subscriber_name;
        $subscriber_email = $request->subscriber_email;
        
        $check = Newsletter::where('subscriber_email',$subscriber_email)->get();
        if(count($check)==0){
            $newsletter= new Newsletter;
            $newsletter->subscriber_name    = $subscriber_name; 
            $newsletter->subscriber_email   = $subscriber_email;
            $newsletter->save();
            $newsletter_message="Thank you Subscribing with us";
            return back()->withInput($newsletter_message);
        }else{
            $newsletter_message = "Duplicate Email";
            return back()->withInput(['newsletter_message'=>'Duplicate Email']);
        }
    }

